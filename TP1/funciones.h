

/** \brief Pide un numero y lo devuelve
 *
 * \param mensaje[] char Mensaje a mostrar al usuario
 * \return float El numero que ingreso el usuario.
 *
 */
float pedirNumero(char mensaje[]);


/** \brief Suma dos numeros, flotantes o enteros.
 *
 * \param Numero flotante o entero que ingreso el usuario.
 * \param Numero flotante o entero que ingreso el usuario.
 * \return La suma de los dos numeros.
 *
 */
float hacerSuma(float, float);



/** \brief Restar dos numeros, flotantes o enteros.
 *
 * \param float Numero flotante o entero que ingreso el usuario.
 * \param float Numero flotante o entero que ingreso el usuario.
 * \return float La resta de los dos numeros.
 *
 */
float hacerResta(float, float);



/** \brief Divide dos numero, flotantes o enteros.
 *
 * \param float Numero flotante o entero que ingreso el usuario.
 * \param float Numero flotante o entero que ingreso el usuario.
 * \return float La division de los dos numeros.
 *
 */
float hacerDivision(float, float);



/** \brief Multiplica dos numeros, flotantes o enteros.
 *
 * \param float Numero flotante o entero que ingreso el usuario.
 * \param float Numero flotante o entero que ingreso el usuario.
 * \return float La multiplicacion de los dos numeros.
 *
 */
float hacerMultiplicacion(float, float);



/** \brief Sacar el factorial de un numero.
 *
 * \param int Numero entero.
 * \return int El factorial del numero ingresado.
 *
 */
int hacerFactorial(int);
